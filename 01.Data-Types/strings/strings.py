# You can use single/double quotations around strings

# Using single quotations
# allows you to use double quotations as part of the string
name = 'Isaac Muendo'
print(name)

# Using double quotations
# allows you to use single quotations as part of the string
name = "Isaac Muendo"
print(name)

role_model = "Nelson Mandela"
message = '"If you talk to a man in language he understands, that goes to his head. If you talk to him in his own language, that goes to his heart."'
print(role_model +" once said, "+ message)