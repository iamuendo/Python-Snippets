name = "isaac muendo"
message = "would you like to learn some Python today?"

# Use the title() method to display the text in titlecase
# Titlecase is where each word begins with a capital letter.
print("Hello " + name.title() + ", " + message)

# upper() method changes text to Uppercase letters
print("Hello " + name.upper() + ", " + message.upper())

# lower() method changes text to lowercase letters 
# It is used more often when storing data
print("Hello " + name.lower() + ", " + message)